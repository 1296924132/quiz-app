def is_subheading(text):
    """判断是否为小标题"""
    import re
    if not text or not text.strip():
        return False
    t = text.strip()
    # 匹配：一、 / （一）/ 1. / 步骤一 / 第一阶段 / 四、 / 等
    patterns = [
        r'^[一二三四五六七八九十]+[、．\.,\s]',
        r'^（[一二三四五六七八九十]+）',
        r'^\d+[、．\.\-\s]',
        r'^步骤[一二三四五六七八九十]',
        r'^第[一二三四五六七八九十]',
        r'^第[一二三四五六七八九十]+阶段',
        r'^[一二三四五六七八九十]、',
    ]
    for p in patterns:
        if re.match(p, t):
            return True
    # 匹配：【案例推演】这类
    if t.startswith('【') and t.endswith('】'):
        return True
    return False


def write_cell_content(cell, text):
    """
    在单元格中写入多行内容。
    自动识别小标题加粗，正文不加粗。
    首行缩进仅应用于正文段落。
    """
    lines = text.split('\n')
    paras = cell.paragraphs

    # 清空所有段落
    first = True
    para_idx = 0

    for line in lines:
        stripped = line.strip()

        # 获取或创建段落
        if para_idx < len(paras):
            para = paras[para_idx]
            # 清空这个段落
            for r in para.runs:
                r.text = ''
            para.text = ''
        else:
            para = cell.add_paragraph()

        pf = para.paragraph_format
        pf.space_before = Pt(0)
        pf.space_after = Pt(0)
        pf.line_spacing = LINE_SPACING
        pf.alignment = WD_ALIGN_PARAGRAPH.LEFT

        if not stripped:
            # 空行 – 软分隔
            para_idx += 1
            continue

        # 判断是否为小标题
        sub = is_subheading(stripped)

        # 首行缩进：正文用，小标题不用
        if sub:
            pf.first_line_indent = None
        else:
            pf.first_line_indent = INDENT_2CHAR

        # 创建run
        if para.runs:
            run = para.runs[0]
        else:
            run = para.add_run()

        run.text = stripped
        run.font.name = FONT_NAME
        run._element.rPr.rFonts.set(qn('w:eastAsia'), FONT_NAME)
        run.font.size = FONT_SIZE
        # 小标题加粗（美观），正文不加粗
        run.bold = sub

        para_idx += 1


def add_label_cell(cell, text):
    """设置左列标签单元格：宋体12pt加粗居中，垂直居中"""
    # 清空
    for p in cell.paragraphs:
        for r in p.runs:
            r.text = ''
        p.text = ''
    p = cell.paragraphs[0]
    pf = p.paragraph_format
    pf.alignment = WD_ALIGN_PARAGRAPH.CENTER
    pf.space_before = Pt(0)
    pf.space_after = Pt(0)
    pf.line_spacing = LINE_SPACING
    set_cell_vertical_center(cell)
    set_cell_border(cell)
    set_cell_margins(cell)

    if p.runs:
        run = p.runs[0]
    else:
        run = p.add_run()
    run.text = text
    run.font.name = FONT_NAME
    run._element.rPr.rFonts.set(qn('w:eastAsia'), FONT_NAME)
    run.font.size = FONT_SIZE
    run.bold = True


